function checkIfNumbersAreEqual(number1, number2) {
    if (number1 === number2) {
        return true;
    }
    return false;
}

//function to return greater number of two numbers
function max(number1, number2) {
    if (number1 > number2) {
        return number1;
    }
    return number2;
}
    

// Function to take two numbers
// and return the greater number of the two 
function greaterNumber(number1, number2) {
    if (number1 > number2) {
        return number1;
    }
    return number2;
}